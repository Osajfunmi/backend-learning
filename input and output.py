my_age = int(input("enter your age:"))
sister_age = int(input("enter your sister's age:"))

print("am i the same age as my sister?", my_age == sister_age)
print("am i younger than my sister?", my_age < sister_age)
print("am i older or equal to my sisters age?", my_age >= sister_age)
print("is my sister not the same age as me?", sister_age != my_age)
